    private void jtf_courseIDKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtf_courseIDKeyTyped
        // TODO add your handling code here:
        char c=evt.getKeyChar();
        if(!(Character.isAlphabetic(c) || c==KeyEvent.VK_BACK_SPACE || c==KeyEvent.VK_DELETE)){
            evt.consume();
            java.awt.Toolkit.getDefaultToolkit().beep();
        }
    }//GEN-LAST:event_jtf_courseIDKeyTyped

    private void jbt_createExaminerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbt_createExaminerActionPerformed
        // TODO add your handling code here:
        if(jtf_course_name.getText().isEmpty()==false&&jtf_courseID.getText().length()==3&&jta_courseDescription.getText().isEmpty()==false){
            course course = new course();
            course.setcourseName(jtf_course_name.getText());
            course.setcourseID(jtf_courseID.getText().toUpperCase());
            course.setcourseDescription(jta_courseDescription.getText());
            if(JOptionPane.showConfirmDialog(null, "Are you sure you want to create this record with the following information:\nCourse ID: "+course.getcourseID()+"\nCourse name: "+course.getcourseName()+"\nCourse description: "+course.getcourseDescription(), "CREATE RECORD CONFIRMATION", HEIGHT)==0){
                        maintainExamSys.createCourse(course);
            }
            else
                JOptionPane.showMessageDialog(null, "Create record fail!", "CREATE RECORD FAIL", HEIGHT);
        }
        else
            JOptionPane.showMessageDialog(null, "Create record fail! Please fill in all form before you decide to create record.", "CREATE RECORD FAIL", HEIGHT);
        
    }//GEN-LAST:event_jbt_createExaminerActionPerformed

