    private void jbt_removeSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbt_removeSubjectActionPerformed
        // TODO add your handling code here:
        int isConfirmed = 0;
        isConfirmed = JOptionPane.showConfirmDialog(null, "Are you sure you want to remove this record?", "REMOVE RECORD CONFIRMAITON", HEIGHT);
        if(isConfirmed==0){
            maintainExamSys.removeRecord(subject.getSubjectID(), "SUBJECT");
            searchSubject examiner = new searchSubject();
            examiner.closeFrame(frame);
        }
        else{
            JOptionPane.showMessageDialog(null, "Remove record fail!", "REMOVE RECORD FAIL", HEIGHT);
        }
    }//GEN-LAST:event_jbt_removeSubjectActionPerformed

    
    

