    private void jbt_removeCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbt_removeCourseActionPerformed
        // TODO add your handling code here:
        int isConfirmed = 0;
        isConfirmed = JOptionPane.showConfirmDialog(null, "Are you sure you want to remove this record?", "REMOVE RECORD CONFIRMAITON", HEIGHT);
        if(isConfirmed==0){
            maintainExamSys.removeRecord(course.getcourseID(), "COURSE");
            searchCourse sstaff = new searchCourse();
            sstaff.closeFrame(frame);
        }
        else{
            JOptionPane.showMessageDialog(null, "Remove record fail!", "REMOVE RECORD FAIL", HEIGHT);
        }
    }//GEN-LAST:event_jbt_removeCourseActionPerformed

